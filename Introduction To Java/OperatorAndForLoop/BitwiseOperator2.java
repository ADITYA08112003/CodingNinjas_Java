package OperatorAndForLoop;

public class BitwiseOperator2 {

	public static void main(String[] args) {
		int a=10;
//		left shift by one
		System.out.println(a<<1);
//		right shift by one
		System.out.println(a>>1);
		
//		left shift by two
		System.out.println(a<<2);
//		right shift by two
		System.out.println(a>>2);

		
	}

}
