package GettingStartedWithJava;

public class MoreOnHelloWorld {
	public static void main(String[] args) {
//		Output of that code is in same line like HelloHello
		System.out.print("Hello");
		System.out.print("Hello");
		
//		Output of that code is in next line
//		using of "\n" to be break the line
		System.out.print("Hello");
		System.out.print("\n");
		System.out.print("Hello");
		System.out.print("\n");
		
//		using of "\n" in Hello
		System.out.print("Hello\n");
		System.out.print("Hello\n");
//		Using the println is print a new line at the end
		System.out.println("Aditya");
		System.out.println("Shitanshu");
		
//		// is single line comment
//		/**/ is multiple line comment or blocked comment
	}
}
