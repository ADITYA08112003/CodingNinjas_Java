package GettingStartedWithJava;

public class MoreOnVariableAndDataType {
	public static void main(String[] args) {
//		Integer Type Data
		
//		int have 4 bytes = 32 bits
		int a=10;
		System.out.println(a);
//		short have 2 bytes = 16 bits
		short a1=1912;
		System.out.println(a1);
//		long have 8 bytes = 64 bits
		long a2=3198319;
		System.out.println(a2);
//		byte have 1 bytes = 8 bits
		byte a3=12;
		System.out.println(a3);
		
//		Decimal Type Data
		
//		double is more precision than float
		
//		double have 8 bytes = 64 bits
		double d=102.131;
		System.out.println(d);
//		float have 4 bytes = 32 bits
		float f1=10.5f;
		System.out.println(f1);
		
//		Character Data Type
		
//		char have 2 bytes = 16 bits
//		char are represented in '' (single quotes)
		char c='a';
		System.out.println(c);
		
//		Boolean DataType
		
//		boolean just a binary number like have only two value 
//		boolean have 1 bits
		boolean s1=true;
		boolean s2=false;
		System.out.println(s1);
		System.out.println(s2);
		
	}
}
